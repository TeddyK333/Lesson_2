import math

pi = 3.14
print(round(pi))  # rounds the number - 3
print(math.ceil(pi))  # to the closest (rounds it up) - 4
print(math.floor(pi))  # rounds it down 3
print(abs(pi))  # |+-3.14| = 3.14
print(pow(pi, 2))  # 9.8596
print(math.sqrt(441))  # 21
